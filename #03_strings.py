# Strings são textos dentro de aspas

print('amigo você sabe me informar onde tem uma farmacia')

# 'amigo você sabe me informar onde tem uma farmácia' é a string.

# É possível utilizar a barra invertida '\' para que uma aspa não seja considera-
# da para fechar a string, caso necessário.

print("que mexe com \"motor\" a diesel")

# Caso queira que a barra invertida apareça no texto, utilize o argumento 'r'

print(r"porque desde \"quarta\" nao consigo entrar no aplicativo")

# Porém, não é recomendado utilizar estas funções por "poluírem" o código, sendo
# útil o uso da inversão de aspas para o mesmo propósito.

print('ta dizendo que ta em "analise"')
