# Para usar o separador, preciso separar os argumentos por vírgula e colocá-los entre aspas

print('voce', 'pensa', 'que', 'o', 'flamengo', 'é', 'time', sep='-')

# Posso usar quaisquer caracteres como separadores

print('flamengo', 'nao', 'é', 'time', 'nao', sep='WOLACE')

# \r\n = CRLF (Quebra de linha), se substituídos em 'end=', a linha não será quebrada

print('time', 'é', 'o', 'vasco', 'da', 'gama', sep='VICE', end='OOO')
print('flamengo', 'é', 'seleção', 'porra', sep='UMAVEZ')
